'''读入一个字典类型的字符串，反转其中键值对输出。‪‬‪‬‪‬‪‬‪‬‮‬‪‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‭‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‪‬‪‬‪‬‪‬‪‬‪‬‮‬‭‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬

即，读入字典key:value模式，输出value:key模式。输入{"a": 1, "b": 2}输出 {1: 'a', 2: 'b'}'''
s=input()
d1={}
d1=eval(s)
if type(d1)!=dict:
    print('输入错误')
else:
    d2={}
    for i in d1.items():
        name=i[0]
        value=i[1]
        d2[value]=name
    print(d2)


